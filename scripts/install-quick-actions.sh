# ScreenRec para convertir el/los vídeo(s) seleccionado(s):
#   · «Convertir vídeo con ScreenRec»  → ScreenRec --convert-video
#   · «Convertir a GIF con ScreenRec»  → ScreenRec --convert-gif
#
# Las Acciones rápidas las vende Automator (firmado por Apple), por eso aparecen
# en el clic derecho aunque ScreenRec tenga firma ad-hoc.
